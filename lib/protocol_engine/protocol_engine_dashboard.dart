  State<ProtocolScreen> createState() => _ProtocolScreenState();
}

class _ProtocolScreenState extends State<ProtocolScreen> {
  final ScrollController _scrollController = ScrollController();
  bool _showTitle = false;
  late final ProtocolEngine protocol;

  @override
  void initState() {
    super.initState();
    protocol = getDummyProtocol();
    _scrollController.addListener(() {
      if (_scrollController.offset > 60 && !_showTitle) {
        setState(() => _showTitle = true);
      } else if (_scrollController.offset <= 60 && _showTitle) {
        setState(() => _showTitle = false);
      }
    });
  }

  @override
  void dispose() {
    _scrollController.dispose();
    super.dispose();
  }

