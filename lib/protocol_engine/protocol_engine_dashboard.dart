  }

  Widget _buildHealthAreaCard({
    required HealthArea area,
    required bool isDark,
  }) {
    IconData trendIcon;
    switch (area.trendType) {
      case TrendType.up:
        trendIcon = Icons.trending_up;
        break;
      case TrendType.flat:
        trendIcon = Icons.trending_flat;
        break;
      case TrendType.down:
        trendIcon = Icons.trending_down;
        break;
    }

    return Container(
      padding: const EdgeInsets.all(16),
      decoration: BoxDecoration(
        color: isDark ? const Color(0xFF1A2C24) : Colors.white,
        borderRadius: BorderRadius.circular(12),
        border: Border.all(color: Colors.transparent),
        boxShadow: [
          BoxShadow(
            color: Colors.black.withOpacity(0.04),
            blurRadius: 20,
            offset: const Offset(0, 4),
          ),
        ],
      ),
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        mainAxisAlignment: MainAxisAlignment.spaceBetween,
        children: [
          Container(
            width: 40,
            height: 40,
            decoration: BoxDecoration(
              color: area.color.withOpacity(0.1),
              borderRadius: BorderRadius.circular(20),
            ),
            child: Icon(area.icon, color: area.color, size: 24),
          ),
          Column(
            crossAxisAlignment: CrossAxisAlignment.start,
            children: [
              Text(
                area.name.toUpperCase(),
                style: TextStyle(
                  color: const Color(0xFF618975),
                  fontSize: 11,
                  fontWeight: FontWeight.w600,
                  letterSpacing: 1,
                ),
              ),
              const SizedBox(height: 4),
              Text(
                area.status,
                style: const TextStyle(
                  fontSize: 20,
                  fontWeight: FontWeight.bold,
                ),
              ),
              const SizedBox(height: 4),
              Row(
                children: [
                  Icon(trendIcon, color: area.color, size: 14),
                  const SizedBox(width: 4),
                  Text(
                    area.trend,
                    style: TextStyle(
                      color: area.color,
                      fontSize: 12,
                      fontWeight: FontWeight.w500,
                    ),
                  ),
                ],
              ),
            ],
          ),
        ],
      ),
    );
  }

  Widget _buildProtocolPhases(bool isDark) {
    return Column(
      crossAxisAlignment: CrossAxisAlignment.start,
      children: [
        const Text(
          'Protocol Phases',
          style: TextStyle(
            fontSize: 18,
            fontWeight: FontWeight.bold,
            letterSpacing: -0.5,
          ),
        ),
        const SizedBox(height: 16),
        Stack(
          children: [
            // Vertical line
            Positioned(
              left: 30,
              top: 24,
              bottom: 24,
              child: Container(
                width: 2,
                color: isDark
                    ? Colors.white.withOpacity(0.1)
                    : Colors.grey.shade200,
              ),
            ),
            // Phases
            Column(
              children: protocol.protocolPhases.asMap().entries.map((entry) {
                final index = entry.key;
                final phase = entry.value;
                final isCompleted = protocol.isPhaseCompleted(phase);
                final isActive = protocol.currentPhase == phase;
                final progress = isActive
                    ? protocol.getPhaseProgress(phase)
                    : null;

                return Padding(
                  padding: EdgeInsets.only(
                    bottom: index < protocol.protocolPhases.length - 1 ? 32 : 0,
                  ),
                  child: _buildPhase(
                    phase: phase,
                    isCompleted: isCompleted,
                    isActive: isActive,
                    progress: progress,
                    isDark: isDark,
                  ),
                );
              }).toList(),
            ),
          ],
        ),
      ],
    );
  }

  Widget _buildPhase({
    required ProtocolPhase phase,
    required bool isCompleted,
    required bool isActive,
    required bool isDark,
    double? progress,
  }) {
    return Opacity(
      opacity: (isCompleted && !isActive) || (!isActive && !isCompleted)
          ? 0.5
          : 1.0,
      child: Row(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          // Phase indicator
          Container(
            width: 48,
            height: 48,
            decoration: BoxDecoration(
              color: isCompleted
                  ? const Color(0xFF11D473)
                  : isActive
                  ? (isDark ? const Color(0xFF1A2C24) : Colors.white)
                  : (isDark
                        ? Colors.white.withOpacity(0.05)
                        : Colors.grey.shade100),
              shape: BoxShape.circle,
              border: isActive
                  ? Border.all(color: const Color(0xFF11D473), width: 4)
                  : Border.all(
                      color: isDark
                          ? const Color(0xFF102219)
                          : const Color(0xFFF6F8F7),
                      width: 4,
                    ),
              boxShadow: isActive || isCompleted
                  ? [
                      BoxShadow(
                        color: const Color(0xFF11D473).withOpacity(0.3),
                        blurRadius: 15,
                      ),
                    ]
                  : null,
            ),
            child: Icon(
              isCompleted
                  ? Icons.check
                  : isActive
                  ? Icons.play_arrow
                  : Icons.lock_outline,
              color: isCompleted
                  ? Colors.white
                  : isActive
                  ? const Color(0xFF11D473)
                  : Colors.grey,
              size: 24,
            ),
          ),
          const SizedBox(width: 16),
          // Phase details
          Expanded(
            child: Column(
              crossAxisAlignment: CrossAxisAlignment.start,
              children: [
                Row(
                  children: [
                    Text(
                      phase.title,
                      style: const TextStyle(
                        fontSize: 16,
                        fontWeight: FontWeight.bold,
                      ),
                    ),
                    if (isActive) ...[
                      const SizedBox(width: 8),
                      Container(
                        padding: const EdgeInsets.symmetric(
                          horizontal: 8,
                          vertical: 2,
                        ),
                        decoration: BoxDecoration(
                          color: const Color(0xFF11D473),
                          borderRadius: BorderRadius.circular(12),
                        ),
                        child: const Text(
                          'CURRENT',
                          style: TextStyle(
                            color: Colors.white,
                            fontSize: 10,
                            fontWeight: FontWeight.bold,
                            letterSpacing: 0.5,
                          ),
                        ),
                      ),
                    ],
                  ],
                ),
                const SizedBox(height: 4),
                Text(
                  '${phase.range} • ${phase.summary}',
                  style: TextStyle(
                    color: isDark
                        ? Colors.grey.shade400
                        : const Color(0xFF618975),
                    fontSize: 14,
                  ),
                ),
                if (progress != null) ...[
                  const SizedBox(height: 8),
                  SizedBox(
                    width: 160,
                    child: ClipRRect(
                      borderRadius: BorderRadius.circular(8),
                      child: LinearProgressIndicator(
                        value: progress,
                        backgroundColor: isDark
                            ? Colors.white.withOpacity(0.1)
                            : Colors.grey.shade200,
                        valueColor: const AlwaysStoppedAnimation<Color>(
                          Color(0xFF11D473),
                        ),
                        minHeight: 6,
                      ),
                    ),
                  ),
                ],
              ],
            ),
          ),
        ],
      ),
    );
  }
