  Widget _buildHealthAreas(bool isDark) {
    return Column(
      children: [
        Row(
          mainAxisAlignment: MainAxisAlignment.spaceBetween,
          children: [
            const Text(
              'Key Health Areas',
              style: TextStyle(
                fontSize: 18,
                fontWeight: FontWeight.bold,
                letterSpacing: -0.5,
              ),
            ),
            Text(
              'VIEW ALL',
              style: TextStyle(
                color: const Color(0xFF11D473),
                fontSize: 12,
